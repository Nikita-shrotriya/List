#remove method
a=["nikki","rani","aayu","himmi"]
a.remove("rani")
print(a)

#pop method for removing item
a=["nikki","rani","aayu","himmi"]
a.pop()
print(a)

#del method for removing items
a=["nikki","rani","aayu","himmi"]
del a[2]
print(a)

#clear method for removing items
a=["nikki","rani","aayu","himmi"]
a.clear()
print(a)